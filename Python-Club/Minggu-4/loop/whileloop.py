#Contoh penggunaan While Loop
#Catatan: Penentuan ruang lingkup di Python bisa menggunakan tab alih-alih menggunakan tanda kurung

count = 0
while (count < 9): # Selama variabel count kurang dari 5 (Kondisi True)
    print("The count is: ", count)
    count += 1 # Setiap kali loop dijalankan, variabel count akan ditambah 1

print("Good bye!")